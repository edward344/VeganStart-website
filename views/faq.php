    <p class="question">Q - Why Focus on Breakfast?</p>
    <p>One of our goals is to encourage new habits and for people to make a daily change. Most breakfast choices 
    are relatively limited: typically cereal, toast or oatmeal/porridge, so this makes it the most practical meal
    to target and provide helpful info for.</p>
    
    <p class="question">Q – Why Encourage Veganism?</p>
    <p>Our main interest at VeganStart is to encourage greater consideration for all animal on earth and to
    reconsider our relationship to them, as inspired by The Vegan Society. There are many reasons for considering
    veganism, which can benefit the well-being of the planet and your own health, and whatever the inspiration, 
    we’re here to help you give it a go. We discuss veganism in more depth here.</p>
    
    <p class="question">Q – Are You Going to Post Gorey Images?</p>
    <p>Nope, our goal is to educate, not horrify. We believe the many merits of veganism are enough to inspire
    change and see no need to resort to shock tactics.</p>
    
    <p class="question">Q – How About Lunch & Dinner?</p>
    <p>One step at a time, young grasshopper! We believe that once breakfasts are conquered, lunch and dinner 
    will be much easier to ‘convert’. The habits picked up at breakfast will carry on through the day. For
    example, if you’re putting non-dairy milk in your coffee in the morning, there’s a good chance that you
    will carry that on through the rest of the day. That said, we fully encourage experimenting with vegan meals
    and snacks the rest of the day too!</p>
    
