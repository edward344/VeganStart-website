    <p class="question">Q – What Inspired This Project?</p>
    <p>Having spoken with many, many people interested in going vegan over the last few decades, it is clear 
    there are many degrees of commitment. Some are ready to give it a go right off the top and commit fully or
    try out a 7 Day or 4 Week ‘Vegan Kickstart’; while others might need something like Meatless Monday to
    ease into it, but there isn’t much in-between. We hope this approach appeals to those people who have yet
    to find what suits them and that it presents an entry point to veganism that works for them.</p>
    
    <p class="question">Q – My Vegan Start Has Been Great! What’s Next?</p>
