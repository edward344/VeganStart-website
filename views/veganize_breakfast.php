    <ul>
        <li><b>Soy milk</b> is one of the ‘originals’ and often the most nutritious, offering more protein and fibre
        than most, as well as beneficial phytonutrients. Some brands are minimally processed and contain not 
        much more than ground soy beans. Others can barely be called soy milk and are a highly processed mixture 
        of soy protein powder, sugar and flavouring. The taste of this mixture can be very controlled and might
        be more familiar, but try to aim for a more quality product. Make sure you go with organic soy products;
        they contain fewer pesticides and are your best option if you’re concerned with GMOs.</li>
        <br>
        <li><b>Almond milk</b> is the next most common contender, overtaking soy in some areas. It’s typically more 
        watery and light and has a less desirable nutrition profile with typically fewer calories and fat per 
        serving.</li>
        <br>
        <li><b>Rice milk</b> used to be #2 to soy but has fallen in popularity to almond. It offers a bit more 
        nutrition than almond and a similar watery consistency, but more calories than most others</li>
        <br>
        <li><b>Coconut milk</b> is becoming very popular, likely due to its fatty nature! It’s also less 
        nutritious but offers more fat than almond milk, although a similar number of calories.</li>
        <br>
        <li>The list goes on … all these are commercial non-dairy milks that can be found on store shelves: 
        <b>flax</b> milk, <b>hempseed</b> milk, <b>oat</b> milk, <b>hazelnut</b> milk, <b>sunflower</b> milk,
        <b>quinoa</b> milk, <b>potato</b> milk, <b>cashew</b>
        milk and <b>macadamia nut</b> milk. In addition to blends featuring two or more of the above, or made with 
        mixtures of grains. Each has it’s own flavour and characteristics.  Sample them all to find your 
        favourites!</li>
    </ul>
    
